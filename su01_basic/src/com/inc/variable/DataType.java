package com.inc.variable;

public class DataType {

	public static void main(String[] args) {
		//변수에 데이터를 저장할 때 변수 타입과 데이너틔 타입이 일치해야 한다.
		//100 - int
		//0.1 - double
		//'가' - char
		//1L - long
		//"사과" - String
		//{'1', '2', '3'} - char[]
		//{"10", "20", "30"} - String[]
		//012123 - 8진법 정수(int)
		//0x0aaaa - 16진법 정수(int)
		int i = 0x1a;
		System.out.println(i);
	}

}
