package com.inc.ms2;

public class Public06 {

	public static void main(String[] args) {
		float firstNum=13.9f;
		float secondNum=3.1f;
		float resultNum=0.5f;
		
		resultNum=firstNum+secondNum;
		System.out.println(resultNum);
		
		resultNum=firstNum-secondNum;
		System.out.println(resultNum);
		
		resultNum=firstNum*secondNum;
		System.out.println(resultNum);
		
		resultNum=firstNum/secondNum;
		System.out.println(resultNum);
	}

}
