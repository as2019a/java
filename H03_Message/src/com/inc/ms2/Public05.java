package com.inc.ms2;

public class Public05 {

	public static void main(String[] args) {
		int firstNum=10;
		int secondNum=7;
		int resultNum=0;
		
		resultNum=firstNum+secondNum;
		System.out.println(resultNum);
		
		resultNum=firstNum-secondNum;
		System.out.println(resultNum);
		
		resultNum=firstNum*secondNum;
		System.out.println(resultNum);
		
		resultNum=firstNum/secondNum;
		System.out.println(resultNum);
		
		resultNum=firstNum%secondNum;
		System.out.println(resultNum);
		
	}

}
